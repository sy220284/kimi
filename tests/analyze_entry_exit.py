"""
科技板块买卖点深度分析报告
"""
import pandas as pd

# 读取数据
df = pd.read_csv('tests/results/techtrade_details_20260318_1053.csv')
df['is_win'] = df['pnl_pct'] > 0

print("="*80)
print("📈 科技板块买卖点深度分析报告")
print("="*80)

# 一、买入时机分析
print("\n🎯 一、买入时机分析（入场点特征）")
print("-"*80)

wavestats = df.groupby('entry_wave').agg({
    'pnl_pct': ['count', 'mean', 'sum'],
    'holding_days': 'mean',
    'is_win': 'mean'
}).round(2)
wavestats.columns = ['交易次数', '平均收益%', '总收益%', '平均持仓天', '胜率']

print("""
┌─────────────────────────────────────────────────────────────────────────┐
│                      按入场波浪类型统计                                  │
├──────────┬──────────┬──────────┬──────────┬──────────┬──────────────────┤
│ 入场波浪  │ 交易次数  │ 平均收益% │ 总收益%   │ 平均持仓  │ 胜率             │
├──────────┼──────────┼──────────┼──────────┼──────────┼──────────────────┤""")
for wave, row in wavestats.iterrows():
    print(f"│ Wave {wave:<3} │ {row['交易次数']:>8.0f} │ {row['平均收益%']:>8.2f} │ {row['总收益%']:>8.2f} │ {row['平均持仓天']:>8.1f} │ {row['胜率']:>8.1%} │")
print("""└──────────┴──────────┴──────────┴──────────┴──────────┴──────────────────┘

💡 关键发现：
  • Wave 2 入场：平均收益 +7.55%，持仓更长(88天)，适合趋势跟踪
  • Wave C 入场：交易频率高(727次)，但平均收益仅+1.81%，偏震荡
""")

# 二、卖出时机分析
print("\n\n💰 二、卖出时机分析（出场点特征）")
print("-"*80)

# 简化卖出原因分类
df['exit_simple'] = df['exit_reason'].apply(lambda x: 'trailing_stop' if 'trailing_stop' in str(x) else str(x))

exitstats = df.groupby('exit_simple').agg({
    'pnl_pct': ['count', 'mean', 'sum', 'std'],
    'holding_days': 'mean',
    'is_win': 'mean'
}).round(2)
exitstats.columns = ['次数', '平均收益%', '总收益%', '波动率', '平均持仓', '胜率']

print("""
┌─────────────────────────────────────────────────────────────────────────┐
│                      按卖出原因统计                                      │
├──────────────┬──────────┬──────────┬──────────┬──────────┬──────────────┤
│ 卖出方式      │ 次数      │ 平均收益% │ 总收益%   │ 平均持仓  │ 胜率         │
├──────────────┼──────────┼──────────┼──────────┼──────────┼──────────────┤""")
for reason, row in exitstats.sort_values('平均收益%', ascending=False).iterrows():
    name = reason[:12]
    print(f"│ {name:<12} │ {row['次数']:>8.0f} │ {row['平均收益%']:>8.2f} │ {row['总收益%']:>9.2f} │ {row['平均持仓']:>8.1f} │ {row['胜率']:>8.1%} │")
print("""└──────────────┴──────────┴──────────┴──────────┴──────────┴──────────────┘

💡 关键发现：
  • 移动止盈：平均收益 +20.8%，胜率 93.5%，策略核心利润来源
  • 止损：平均亏损 -9.5%，胜率 0%，必须接受的成本
""")

# 三、最佳/最差交易分析
print("\n\n🏆 三、最佳交易 TOP 10")
print("-"*80)

top10 = df.nlargest(10, 'pnl_pct')[['symbol', 'entry_date', 'exit_date', 'entry_wave', 'pnl_pct', 'holding_days', 'exit_reason']]
print("""
┌────────┬────────────┬────────────┬──────────┬─────────┬──────────┬─────────────────────┐
│ 股票   │ 买入日期    │ 卖出日期    │ 入场波浪  │ 收益%    │ 持仓天数  │ 卖出方式             │
├────────┼────────────┼────────────┼──────────┼─────────┼──────────┼─────────────────────┤""")
for _, row in top10.iterrows():
    exit_type = row['exit_reason'][:20]
    print(f"│ {row['symbol']:<6} │ {row['entry_date']:<10} │ {row['exit_date']:<10} │ Wave {row['entry_wave']:<4} │ {row['pnl_pct']:>+7.2f} │ {row['holding_days']:>8} │ {exit_type:<20} │")
print("""└────────┴────────────┴────────────┴──────────┴─────────┴──────────┴─────────────────────┘

💡 最佳交易特征：
  • 平均持仓 168天，中位数 82天 → 大钱是拿出来的
  • Wave 2 入场占 60% → 趋势启动点入场
  • 全部是移动止盈卖出 → 让利润奔跑
""")

print("\n\n💩 四、最差交易 TOP 10")
print("-"*80)

bottom10 = df.nsmallest(10, 'pnl_pct')[['symbol', 'entry_date', 'exit_date', 'entry_wave', 'pnl_pct', 'holding_days', 'exit_reason']]
print("""
┌────────┬────────────┬────────────┬──────────┬─────────┬──────────┬─────────────────────┐
│ 股票   │ 买入日期    │ 卖出日期    │ 入场波浪  │ 收益%    │ 持仓天数  │ 卖出方式             │
├────────┼────────────┼────────────┼──────────┼─────────┼──────────┼─────────────────────┤""")
for _, row in bottom10.iterrows():
    exit_type = row['exit_reason'][:20]
    print(f"│ {row['symbol']:<6} │ {row['entry_date']:<10} │ {row['exit_date']:<10} │ Wave {row['entry_wave']:<4} │ {row['pnl_pct']:>+7.2f} │ {row['holding_days']:>8} │ {exit_type:<20} │")
print("""└────────┴────────────┴────────────┴──────────┴─────────┴──────────┴─────────────────────┘

💡 最差交易特征：
  • 全部是止损卖出 → 策略纪律执行
  • Wave 2 入场占 80% → 趋势判断错误
  • 平均持仓 169天(含超长套牢) → 没有及时止损
  • 中兴通讯(000063)2017-10买入，2018-06才止损，-58%
""")

# 四、持仓时间分析
print("\n\n⏱️ 五、持仓时间分析")
print("-"*80)

df['holding_bucket'] = pd.cut(df['holding_days'], 
                               bins=[0, 5, 20, 60, 150, 500], 
                               labels=['超短(1-5天)', '短(6-20天)', '中(21-60天)', '长(61-150天)', '超长(150天+)'])

holdingstats = df.groupby('holding_bucket').agg({
    'pnl_pct': ['count', 'mean', 'sum'],
    'is_win': 'mean'
}).round(2)
holdingstats.columns = ['次数', '平均收益%', '总收益%', '胜率']

print("""
┌─────────────────────────────────────────────────────────────────────────┐
│                      按持仓时间统计                                      │
├────────────────┬──────────┬──────────┬──────────┬────────────────────────┤
│ 持仓时间        │ 次数      │ 平均收益% │ 总收益%   │ 胜率                   │
├────────────────┼──────────┼──────────┼──────────┼────────────────────────┤""")
for bucket, row in holdingstats.iterrows():
    print(f"│ {bucket:<14} │ {row['次数']:>8.0f} │ {row['平均收益%']:>8.2f} │ {row['总收益%']:>8.2f} │ {row['胜率']:>10.1%}          │")
print("""└────────────────┴──────────┴──────────┴──────────┴────────────────────────┘

💡 关键发现：
  • 超短/短持仓：胜率极低(0-13%)，平均亏损 → 避免频繁交易
  • 中持仓(21-60天)：胜率49%，开始盈利 → 耐心持有
  • 长/超长持仓：胜率60%左右，收益最高 → 趋势跟踪的核心
  • "让利润奔跑"是真实的：持仓越久，平均收益越高
""")

